# ispisat će samo 1 jer range ide samo do zadnje određenog broja ali ne uzima taj zadnji broj u obzir
for i in range(1, 2):
    print(i)

# nema ispisa jer se treba smanjivat ali zbog 2 se povecava konstantno
for i in range(10, 1, 2):
    print(i)

# ispisat će rezultat od 10 do 2 
for i in range(10, 1, -1):
    print(i)





